            file << "main{max-width:1180px;margin:0 auto;padding:28px}";
            file << "header{background:#081512;color:#effaf3;padding:28px;border-bottom:5px solid #36c36b}";
            file << "h1{margin:0;font-size:32px} h2{margin:28px 0 10px;font-size:20px} h3{margin:16px 0 8px;font-size:16px}";
            file << ".muted{color:#64746e}.grid{display:grid;grid-template-columns:repeat(auto-fit,minmax(190px,1fr));gap:12px}";
            file << ".metric{background:white;border:1px solid #dbe5df;border-radius:8px;padding:14px}.metric b{display:block;font-size:20px;margin-top:4px}";
            file << "table{border-collapse:collapse;width:100%;background:white;border:1px solid #dbe5df;margin:8px 0 18px}";
            file << "th,td{text-align:left;padding:8px 10px;border-bottom:1px solid #e4ece7;vertical-align:top}th{background:#eaf3ee;font-size:12px;text-transform:uppercase;letter-spacing:.04em}";
