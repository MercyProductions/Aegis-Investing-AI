    std::filesystem::path ReportsDirectory()
    {
        std::filesystem::path path = AppDataDirectory() / "reports";
        std::error_code ec;
        std::filesystem::create_directories(path, ec);
        return path;
    }

    bool ExportSymbolResearchReport(const SymbolResearchReport& report, std::filesystem::path& output_path, std::string& status)
    {
        output_path = ReportsDirectory() / (Slug(report.quote.symbol) + "-research-report.html");
        std::ofstream file(output_path, std::ios::trunc);
        if (!file)
        {
            status = "Could not write research report.";
            return false;
        }

        BeginDocument(file, report.quote.symbol + " Research Report", report.quote.name + " | generated " + NowTimeLabel());
        file << "<div class=\"grid\">";
        Metric(file, "Price", FormatCurrency(report.quote.price), report.quote.latest_trading_day);
        Metric(file, "Change", FormatPercent(report.quote.change_percent), report.quote.source);
        Metric(file, "Signal", report.signal.rating, report.signal.posture);
        Metric(file, "Score", std::to_string(report.signal.score), std::to_string(report.signal.confidence) + "% confidence");
        Metric(file, "Target", FormatCurrency(report.signal.target_price), "Stop " + FormatCurrency(report.signal.stop_level));
        Metric(file, "Volume", FormatVolume(report.quote.volume), report.quote.status);
        file << "</div>";
        file << "<section class=\"section\"><h2>Thesis</h2><p>" << Html(report.signal.thesis) << "</p></section>";
        file << "<section class=\"section\"><h2>Freshness</h2><table><tbody>";
        file << "<tr><th>History</th><td>" << Html(report.history_source) << "</td><td>" << Html(report.history_freshness) << "</td><td>" << Html(report.history_fallback) << "</td></tr>";
        file << "<tr><th>Research</th><td>" << Html(report.research_source) << "</td><td>" << Html(report.research_freshness) << "</td><td>" << Html(report.research_fallback) << "</td></tr>";
        file << "</tbody></table></section>";
        file << "<section class=\"section\"><h2>Technical Snapshot</h2><div class=\"grid\">";
        Metric(file, "Regime", report.indicators.regime);
        Metric(file, "SMA 20 / 50", FormatCurrency(report.indicators.sma20) + " / " + FormatCurrency(report.indicators.sma50));
        Metric(file, "RSI 14", FormatPercent(report.indicators.rsi14));
        Metric(file, "MACD", FormatCurrency(report.indicators.macd) + " / " + FormatCurrency(report.indicators.macd_signal));
        Metric(file, "ATR 14", FormatCurrency(report.indicators.atr14));
        Metric(file, "Drawdown", FormatPercent(report.indicators.drawdown));
        file << "</div></section>";
        InfoTable(file, "Explainable Score", report.explanation);
        InfoTable(file, "Trade Checklist", report.checklist);
        InfoTable(file, "Risk Critic", report.risk_critic);
        InfoTable(file, "Backtest", report.backtest.rows);
        if (!report.strategy_rule.empty())
        {
            file << "<section class=\"section\"><h2>Strategy Builder</h2><p><b>Rule:</b> " << Html(report.strategy_rule) << "</p><p>" << Html(report.strategy_status) << "</p></section>";
            InfoTable(file, "Strategy Backtest", report.strategy_backtest.rows);
        }
        file << "<section class=\"section\"><h2>Fundamentals</h2><div class=\"grid\">";
        Metric(file, "Revenue", FormatCompactCurrency(report.fundamentals.revenue));
        Metric(file, "EPS", FormatCurrency(report.fundamentals.eps));
        Metric(file, "Margins", FormatPercent(report.fundamentals.gross_margin) + " / " + FormatPercent(report.fundamentals.net_margin));
        Metric(file, "Debt/Equity", FormatPercent(report.fundamentals.debt_to_equity));
        Metric(file, "Free cash flow", FormatCompactCurrency(report.fundamentals.free_cash_flow));
        Metric(file, "P/E / PEG", FormatPercent(report.fundamentals.pe) + " / " + FormatPercent(report.fundamentals.peg));
        file << "</div><p>" << Html(report.fundamentals.summary) << "</p></section>";
        LinkList(file, "SEC Filings", report.filings);
        NewsTable(file, report.news);
        EarningsTable(file, report.earnings);
        EndDocument(file);

        status = "Research report exported: " + output_path.string();
        AppendDiagnosticEvent({ "info", "report", "symbol", report.quote.symbol, "exported", output_path.string(), "", 0, 0, false });
        return true;
    }

    bool ExportDailyBriefingReport(const DailyBriefingReport& report, std::filesystem::path& output_path, std::string& status)
    {
        output_path = ReportsDirectory() / "today-briefing.html";
        std::ofstream file(output_path, std::ios::trunc);
        if (!file)
        {
            status = "Could not write daily briefing.";
            return false;
        }

        BeginDocument(file, "Auralith Today's Briefing", report.state.source_badge + " | generated " + NowTimeLabel());
        InfoTable(file, "Dashboard Metrics", report.state.metrics);
        InfoTable(file, "Research State", report.state.research);
        file << "<section class=\"section\"><h2>Top Signals</h2><table><thead><tr><th>Symbol</th><th>Rating</th><th>Score</th><th>Risk</th><th>Thesis</th></tr></thead><tbody>";
        const int signal_limit = std::min(12, static_cast<int>(report.state.signals.size()));
        for (int i = 0; i < signal_limit; ++i)
        {
            const StockSignal& signal = report.state.signals[static_cast<size_t>(i)];
            file << "<tr><td>" << Html(signal.symbol) << "</td><td>" << Html(signal.rating) << "</td><td>" << signal.score << "</td><td>" << Html(signal.risk) << "</td><td>" << Html(signal.thesis) << "</td></tr>";
        }
        file << "</tbody></table></section>";
        file << "<section class=\"section\"><h2>Today's Focus</h2><table><thead><tr><th>Symbol</th><th>Priority</th><th>Reason</th><th>Detail</th></tr></thead><tbody>";
        for (const FocusItem& item : report.focus)
            file << "<tr><td>" << Html(item.symbol) << "</td><td>" << Html(item.priority) << "</td><td>" << Html(item.reason) << "</td><td>" << Html(item.detail) << "</td></tr>";
        file << "</tbody></table></section>";
        file << "<section class=\"section\"><h2>Alert Events</h2><table><thead><tr><th>Symbol</th><th>State</th><th>Observed</th><th>Triggered</th><th>Note</th></tr></thead><tbody>";
        int events_written = 0;
        for (auto it = report.alert_events.rbegin(); it != report.alert_events.rend() && events_written < 15; ++it, ++events_written)
            file << "<tr><td>" << Html(it->symbol) << "</td><td>" << Html(AlertEventState(*it)) << "</td><td>" << Html(FormatCurrency(it->observed_price)) << "</td><td>" << Html(it->triggered_at) << "</td><td>" << Html(it->note) << "</td></tr>";
        file << "</tbody></table></section>";
        file << "<section class=\"section\"><h2>Holdings</h2><table><thead><tr><th>Symbol</th><th>Shares</th><th>Average Cost</th><th>Note</th></tr></thead><tbody>";
        for (const PortfolioHolding& holding : report.holdings)
            file << "<tr><td>" << Html(holding.symbol) << "</td><td>" << holding.shares << "</td><td>" << Html(FormatCurrency(holding.average_cost)) << "</td><td>" << Html(holding.note) << "</td></tr>";
        file << "</tbody></table></section>";
        EndDocument(file);

        status = "Daily briefing exported: " + output_path.string();
        AppendDiagnosticEvent({ "info", "report", "briefing", "", "exported", output_path.string(), "", 0, 0, false });
        return true;
    }
